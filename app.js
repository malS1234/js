// Операторы присваивания
let age = 10 + 10;
age += 2; // age = age + 2
age -= 3; // age = age - 3
age *= 2; // age = age * 2
age /= 2; // age = age / 2

age++; // age = age + 1
age--; // age = age - 1

console.log(age);

// Операторы сравнения

const denis = 35;
console.log(age > denis);
console.log(age >= denis);
console.log(age < denis);
console.log(age <= denis);
console.log(age == denis);

